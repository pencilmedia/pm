<h4>Web Banners</h4>
<p>
Typically, the purpose of a Web banner ad, like a direct response ad, is to get people to act. Needless to say, a good concept is important. The banner is like any other advertising vehicle in that it must start with a great idea. The idea should have &quot;legs&quot; - it must be useful in a campaign and communicate the offer clearly. At the same time the concept has to be relevant so that it speaks to the audience in their language, and makes propositions that recognize their specific needs and desires. Simplicity is one factor which is an essential in banner advertising and more or less unique to it.
</p>
